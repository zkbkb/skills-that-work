def get_season_strength(wuxing: str, lunar_month: int) -> str:
    """
    判断某五行在某农历月份的旺衰状态。
    返回：'旺'/'相'/'休'/'囚'/'死'
    """
    if lunar_month in EARTH_MONTHS:
        season_data = SEASON_WUXING["四季"]
    else:
        season = LUNAR_MONTH_SEASON.get(lunar_month, "春")
        season_data = SEASON_WUXING[season]

    for strength, wx in season_data.items():
        if wx == wuxing:
            return strength
    return "休"  # fallback


