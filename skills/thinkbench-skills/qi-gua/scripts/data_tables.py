def get_wuxing_relation(a: str, b: str) -> str:
    """
    判断五行 a 对 b 的关系。
    返回：'生'(a生b), '克'(a克b), '被生'(b生a), '被克'(b克a), '比和'(同属性)
    """
    if a == b:
        return "比和"
    if WUXING_SHENG.get(a) == b:
        return "生"
    if WUXING_KE.get(a) == b:
        return "克"
    if WUXING_SHENG.get(b) == a:
        return "被生"
    if WUXING_KE.get(b) == a:
        return "被克"
    return "无直接关系"


