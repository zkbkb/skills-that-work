# 九、年份天干地支速查辅助函数
# ============================================================

def year_to_ganzhi(year: int) -> tuple:
    """
    公历年份 -> (天干, 地支)
    算法：以公元4年为甲子年的基准
    """
    tiangan_idx = (year - 4) % 10
    dizhi_idx = (year - 4) % 12
    return TIANGAN[tiangan_idx], DIZHI[dizhi_idx]


def hour_to_dizhi(hour: int, minute: int = 0) -> str:
    """
    24小时制时间 -> 地支时辰
    注意：23:00 开始为子时
    """
    # 调整：23点开始算下一天的子时
    if hour == 23:
        return "子"
    idx = ((hour + 1) // 2) % 12
    return DIZHI[idx]


