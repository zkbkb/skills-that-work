# ============================================================
# 十、五行旺衰（月建视角）
# ============================================================
# 月建对各爻旺衰的影响
# 月建所属地支的五行，对各爻地支五行构成生克

def get_monthly_strength(yao_wuxing: str, month_dizhi: str) -> str:
    """
    判断某爻五行在某月建下的旺衰。

    Args:
        yao_wuxing: 爻的地支五行
        month_dizhi: 月建地支

    Returns:
        str: 旺/相/休/囚/死
    """
    month_wx = DIZHI_WUXING[month_dizhi]

    # 月建五行当旺
    from .data_tables import WUXING_SHENG

    if yao_wuxing == month_wx:
        return "旺"
    elif WUXING_SHENG.get(month_wx) == yao_wuxing:
        return "相"  # 月建所生
    elif WUXING_SHENG.get(yao_wuxing) == month_wx:
        return "休"  # 生月建者
    # 克月建者 / 被月建克
    from .data_tables import WUXING_KE
    if WUXING_KE.get(month_wx) == yao_wuxing:
        return "囚"  # 被月建所克
    elif WUXING_KE.get(yao_wuxing) == month_wx:
        return "死"  # 克月建（泄气）
    return "休"


def check_yuepo(yao_dizhi: str, month_dizhi: str) -> bool:
    """检查某爻是否被月建所冲（月破）"""
    return DIZHI_CHONG.get(month_dizhi) == yao_dizhi


def check_ri_chong(yao_dizhi: str, day_dizhi: str) -> bool:
    """检查某爻是否被日辰所冲"""
    return DIZHI_CHONG.get(day_dizhi) == yao_dizhi


def check_ri_he(yao_dizhi: str, day_dizhi: str) -> tuple:
    """检查某爻是否与日辰相合，返回 (是否合, 合化五行)"""
    he_info = DIZHI_HE.get(day_dizhi)
    if he_info and he_info[0] == yao_dizhi:
        return True, he_info[1]
    return False, None
