# 三、构建卦名→宫位的反向索引
# ============================================================

def _build_hexagram_gong_index():
    """构建 {卦名: {gong, upper, lower, shi_num, type}} 索引"""
    index = {}
    for gong_name, hexagrams in BAGONG_TABLE.items():
        for name, upper, lower, shi_num, gtype in hexagrams:
            index[name] = {
                "gong": gong_name,
                "upper": upper,
                "lower": lower,
                "shi_num": shi_num,
                "type": gtype,
            }
    return index

HEXAGRAM_GONG_INDEX = _build_hexagram_gong_index()


