"""
yao_ci_data.py — 周易六十四卦爻辞数据库（完整版）
==================================================
覆盖全部 64 卦 × 6 爻 = 384 条爻辞。
每条包含：
  - pos: 爻位 (1-6)
  - name: 爻名 (如 "初九")
  - text: 爻辞原文
  (legacy 'lost' field removed in qi-gua v1.0.0)
"""


def get_yao_ci(hexagram_seq: int, line_position: int) -> dict | None:
    """
    查询某卦某爻的爻辞。

    Args:
        hexagram_seq: 周易卦序 (1-64)
        line_position: 爻位 (1-6)

    Returns:
        dict: {pos, name, text, lost} 或 None（未收录）
    """
    gua = _YAO_CI_DB.get(hexagram_seq)
    if gua is None:
        return None
    for line in gua.get("lines", []):
        if line["pos"] == line_position:
            return line
    return None


def get_gua_ci(hexagram_seq: int) -> dict | None:
    """查询某卦的卦辞、象辞"""
    gua = _YAO_CI_DB.get(hexagram_seq)
    if gua is None:
        return None
    return {
        "name": gua["name"],
        "guaci": gua.get("guaci", ""),
        "xiangci": gua.get("xiangci", ""),
    }


def is_complete(hexagram_seq: int) -> bool:
    """检查某卦的爻辞是否已完整收录"""
    gua = _YAO_CI_DB.get(hexagram_seq)
    if gua is None:
        return False
    return len(gua.get("lines", [])) == 6


def list_complete_hexagrams() -> list:
    """列出已完整收录爻辞的卦"""
    return [seq for seq, data in _YAO_CI_DB.items()
            if len(data.get("lines", [])) == 6]


def get_coverage_stats() -> dict:
    """获取数据库覆盖率统计"""
    complete = list_complete_hexagrams()
    total_lines = sum(len(d.get("lines", [])) for d in _YAO_CI_DB.values())
    return {
        "total_hexagrams": len(_YAO_CI_DB),
        "complete_hexagrams": len(complete),
        "complete_list": complete,
        "total_lines": total_lines,
        "target_lines": 384,
        "coverage": f"{total_lines}/{384} ({total_lines/384*100:.0f}%)",
    }


